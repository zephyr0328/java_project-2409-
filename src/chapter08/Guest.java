package chapter08;

public class Guest {
	//멤버 변수(필드)
	private String name;
	private String gender;
	//생성자
	
	//getter/setter
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	

	
	

}
